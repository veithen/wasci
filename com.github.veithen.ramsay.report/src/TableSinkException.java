public class TableSinkException extends Exception {
    private static final long serialVersionUID = -2112960487141445834L;

    public TableSinkException() {
        super();
    }

    public TableSinkException(String message, Throwable cause) {
        super(message, cause);
    }

    public TableSinkException(String message) {
        super(message);
    }

    public TableSinkException(Throwable cause) {
        super(cause);
    }
}
